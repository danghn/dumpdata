package com.betbrain.sepc.connector.sdql;


/**
 * Listens to a <code>SportsModelConnection</code> for events .
 * 
 * @author Morten Helles
 */

interface PushConnectionListener {

    // --------------------------------------------------------------------------------------------
    // Interface methods
    // --------------------------------------------------------------------------------------------

    public void connecting();

    /* ----------------------------------------------------------------------------------------- */

    public void connected();

    /* ----------------------------------------------------------------------------------------- */
    
    public void connectFailure(Throwable cause);

    /* ----------------------------------------------------------------------------------------- */

    public void disconnecting();

    /* ----------------------------------------------------------------------------------------- */

    public void disconnected();

    /* ----------------------------------------------------------------------------------------- */
    
    public void disconnectFailure(Throwable cause);

    /* ----------------------------------------------------------------------------------------- */
    
    public void reading();

    /* ----------------------------------------------------------------------------------------- */
    
    public void read(SDQLConstruct sdqlConstruct);

    /* ----------------------------------------------------------------------------------------- */

    public void readFailure(Throwable cause);

    /* ----------------------------------------------------------------------------------------- */

    public void writing(SDQLConstruct sdqlConstruct);

    /* ----------------------------------------------------------------------------------------- */
    
    public void wrote(SDQLConstruct sdqlConstruct);

    /* ----------------------------------------------------------------------------------------- */

    public void writeFailure(Throwable cause, SDQLConstruct sdqlConstruct);

    /* ----------------------------------------------------------------------------------------- */
}